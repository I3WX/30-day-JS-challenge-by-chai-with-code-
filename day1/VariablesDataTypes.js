// Declare a variable 'num' and assign it the value 5
var num = 5;
console.log(num); // Output the value of 'num'

// Declare a variable 'string' and assign it the value "String"
let string = "String";
console.log(string); // Output the value of 'string'

// Declare a constant 'boolean' and assign it the value true
const boolean = true;
console.log(boolean); // Output the value of 'boolean'

// Declare an array 'array' with values 1 through 6
let array = [1, 2, 3, 4, 5, 6];

// Declare an object 'object' with properties firstName, lastName, and age
let object = { firstName: "Dan", lastName: "Jonson", age: "25" };

