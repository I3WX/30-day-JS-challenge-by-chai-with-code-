// Declare a variable 'num' and assign it the value 5
var num = 5;

// Declare a variable 'string' and assign it the value "String"
let string = "String";

// Declare a constant 'boolean' and assign it the value true
const boolean = true;

// Declare an array 'array' with values 1 through 6
let array = [1, 2, 3, 4, 5, 6];

// Declare an object 'object' with properties firstName, lastName, and age
let object = { firstName: "Dan", lastName: "Jonson", age: "25" };

// Output the type and value of the variable 'num'
console.log(`${typeof(num)} : ${num}`); // number : 5

// Output the type and value of the variable 'string'
console.log(`${typeof(string)} : ${string}`); // string : String

// Output the type and value of the constant 'boolean'
console.log(`${typeof(boolean)} : ${boolean}`); // boolean : true

// Output the type and value of the variable 'array'
console.log(`${typeof(array)} : ${array}`); // object : 1,2,3,4,5,6

// Output the type and value of the variable 'object'
console.log(`${typeof(object)} : ${object}`); // object : [object Object]
